from __future__ import annotations

import argparse
import json

from .infer import make_backend_payload
from .openheart_interpret import interpret_openheart_outputs, load_openheart_outputs


def build_parser() -> argparse.ArgumentParser:
    p = argparse.ArgumentParser(
        description=(
            "Interpret raw openheart outputs as dlshogi static policy/value evaluation "
            "for a specified shogi position or exported case meta."
        )
    )

    g_outputs = p.add_mutually_exclusive_group(required=True)
    g_outputs.add_argument("--outputs-npz", help="Path to outputs.npz/reference_outputs.npz")
    g_outputs.add_argument("--run-dir", help="Directory containing outputs.npz or *.npy output files")
    g_outputs.add_argument(
        "--policy-logits-npy",
        help="Path to policy_logits.npy; requires --value-logit-npy as well",
    )
    p.add_argument("--value-logit-npy", help="Path to value_logit.npy")

    g_board = p.add_mutually_exclusive_group(required=True)
    g_board.add_argument("--position", help='USI position string, e.g. "position startpos moves 7g7f"')
    g_board.add_argument("--sfen", help="SFEN string")
    g_board.add_argument(
        "--case-meta",
        help="Path to cases/<case_id>/meta.json generated by dlshogi2-export-openheart-package",
    )

    p.add_argument("--temperature", type=float, default=1.0)
    p.add_argument("--topk", type=int, default=10)
    p.add_argument("--include-all-legal", action="store_true")
    p.add_argument("--include-dense-policy", action="store_true")
    p.add_argument("--pretty", action="store_true")
    return p


def main() -> None:
    args = build_parser().parse_args()
    outputs = load_openheart_outputs(
        outputs_npz=args.outputs_npz,
        run_dir=args.run_dir,
        policy_logits_npy=args.policy_logits_npy,
        value_logit_npy=args.value_logit_npy,
    )
    dense, legal, backend = interpret_openheart_outputs(
        outputs=outputs,
        position=args.position,
        sfen=args.sfen,
        case_meta=args.case_meta,
        temperature=args.temperature,
    )
    payload = make_backend_payload(
        backend=backend,
        dense=dense,
        legal=legal,
        include_dense_policy=args.include_dense_policy,
        topk=args.topk,
        include_all_legal=args.include_all_legal,
    )
    if args.pretty:
        print(json.dumps(payload, ensure_ascii=False, indent=2, sort_keys=True))
    else:
        print(json.dumps(payload, ensure_ascii=False))


if __name__ == "__main__":  # pragma: no cover
    main()
